// Slice

const friends = [
  "Santhosh",
  "Chinnakaruppan",
  "Kavinesh",
  " Free4tal",
  "Google",
];

// immuitable

console.log(friends.slice(3));
console.log(friends);

// Splice Muitable

console.log(friends.splice(3));
console.log(friends);

// Reverse Muitable

console.log(friends.reverse());
console.log(friends);

// Join Immutable

console.log(friends.join(" + "));
console.log(friends);

// Concat Immutable

console.log(friends.concat("Santhoshhhhhhhhh"));
console.log(friends);
